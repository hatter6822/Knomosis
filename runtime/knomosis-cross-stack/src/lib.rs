    /// Single-bit flips in the header / record-length fields produce
    /// typed errors rather than panics.  Defends against the
    /// length-confusion class of parser bugs.
    #[test]
    fn single_bit_flips_in_lengths_are_safe() {
        let original = FixtureFile::from_records(FixtureKind::Hash, sample_records());
        let bytes = original.to_bytes();
        // Flip each bit in the first 32 bytes (header + first
        // record-length prefix region) and check no panic results.
        for byte_idx in 0..bytes.len().min(32) {
            for bit_idx in 0..8u32 {
                let mut mutated = bytes.clone();
                mutated[byte_idx] ^= 1 << bit_idx;
                // Result is irrelevant; we're checking we don't
                // panic during decoding.
                let _ = FixtureFile::from_bytes(&mutated);
            }
        }
    }

