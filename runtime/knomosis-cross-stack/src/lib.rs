    /// Records preserve byte-identity (not just structural-eq):
    /// every byte position in the input/expected fields survives
    /// round-trip exactly.
    #[test]
    fn record_bytes_are_byte_identical() {
        let mut input = Vec::with_capacity(257);
        for i in 0..=256u32 {
            // Generates 257 bytes covering every u8 value plus a
            // wraparound so byte-position 0 differs from byte-position
            // 256.
            input.push(u8::try_from(i & 0xff).expect("masked u8"));
        }
        let mut expected = Vec::with_capacity(257);
        for i in 0..=256u32 {
            expected.push(u8::try_from((i ^ 0xab) & 0xff).expect("masked u8"));
        }
        let original = FixtureFile::from_records(
            FixtureKind::Hash,
            vec![FixtureRecord::new(input.clone(), expected.clone())],
        );
        let decoded = FixtureFile::from_bytes(&original.to_bytes()).expect("decode");
        let record = &decoded.records()[0];
        assert_eq!(record.input, input);
        assert_eq!(record.expected, expected);
    }

